    abstract public function getApiSchema();
    abstract public function actionExec(array $params);

    /**
     * 暴露给前端的 Schema 接口
     */
    public function actionGetSchema(array $params)
    {
        return $this->success($this->getApiSchema());
    }

    /**
     * 参数校验接口
     * 校验传入的参数是否符合 API schema 的要求
     */
    public function actionCheckSchema(array $params)
    {
        $business = $params['business'] ?? '';
        $arguments = $params['arguments'] ?? [];

        if (empty($business)) {
            return $this->error("业务标识不能为空");
        }
        if (empty($arguments)) {
            return $this->error("参数不能为空");
        }

        try {
            // 加载配置
            $schemaMap = $this->getApiSchema();
            if (!isset($schemaMap[$business])) {
                throw new LogicException("未定义的业务接口: [{$business}]");
            }
            $config = $schemaMap[$business];

            // 遍历参数定义，逐一校验
            foreach ($config['params'] as $field => $rules) {
                $val = $arguments[$field] ?? null;
                $required = $rules['required'] ?? false;

                // 必填校验
                if ($required && ($val === null || $val === '')) {
                    $required_tip = $rules['required_tip'] ?? "缺少必填参数: {$field}";
                    throw new LogicException($required_tip);
                }

                // 选填且为空，跳过
                if ($val === null) {
                    continue;
                }

                // 类型校验
                $type = $rules['type'] ?? 'string';
                $this->validateValue($val, $type, $field, $rules);
            }

        } catch (\Throwable $e) {
            return $this->error('校验异常: ' . $e->getMessage());
        }
        return $this->success([]);
    }

    /**
     * 值校验器
     * @throws Exception
     */
    public function validateValue($val, string $type, string $field, array $rules): void
    {
        // 基础类型校验
        switch ($type) {
            case 'integer':
                if (!is_numeric($val)) {
                    throw new LogicException("参数 {$field} 必须是数字");
                }
                $numVal = (int)$val;
                if (isset($rules['min']) && $numVal < $rules['min']) {
                    throw new LogicException("参数 {$field} 不能小于 {$rules['min']}");
                }
                if (isset($rules['max']) && $numVal > $rules['max']) {
                    throw new LogicException("参数 {$field} 不能大于 {$rules['max']}");
                }
                break;

            case 'array':
                if (!is_array($val)) {
                    throw new LogicException("参数 {$field} 必须是数组");
                }
                // 数组长度校验
                $count = count($val);
                if (isset($rules['min']) && $count < $rules['min']) {
                    throw new LogicException("参数 {$field} 数组长度不能小于 {$rules['min']}");
                }
                if (isset($rules['max']) && $count > $rules['max']) {
                    throw new LogicException("参数 {$field} 数组长度不能大于 {$rules['max']}");
                }
                // 校验数组元素
                if (isset($rules['items']) && !empty($val)) {
                    foreach ($val as $idx => $item) {
                        $itemRules = $rules['items'];
                        $itemType = $itemRules['type'] ?? 'string';
                        $this->validateValue($item, $itemType, "{$field}[{$idx}]", $itemRules);
                    }
                }
                break;

            case 'object':
                if (!is_array($val)) {
                    throw new LogicException("参数 {$field} 必须是对象");
                }
                break;

            case 'boolean':
                if (!is_bool($val)) {
                    throw new LogicException("参数 {$field} 必须是布尔值");
                }
                break;

            case 'json':
                // 允许 JSON 字符串或数组/对象
                if (is_string($val)) {
                    if (!json_validate($val)) {
                        throw new LogicException("参数 {$field} 必须是有效的 JSON 字符串");
                    }
                } elseif (!is_array($val) && !is_object($val)) {
                    throw new LogicException("参数 {$field} 必须是 JSON 字符串或对象/数组");
                }
                break;

            case 'enum':
                if (!isset($rules['enum']) || !is_array($rules['enum'])) {
                    throw new LogicException("参数 {$field} 的枚举值未定义");
                }
                if (!in_array($val, $rules['enum'], true)) {
                    $allowed = implode(', ', $rules['enum']);
                    throw new LogicException("参数 {$field} 的值必须是: {$allowed} 中的一个");
                }
                break;

            case 'string':
            default:
                if (!is_string($val)) {
                    throw new LogicException("参数 {$field} 必须是字符串");
                }
                // 字符串长度校验
                $len = strlen($val);
                if (isset($rules['min']) && $len < $rules['min']) {
                    throw new LogicException("参数 {$field} 字符串长度不能小于 {$rules['min']}");
                }
                if (isset($rules['max']) && $len > $rules['max']) {
                    throw new LogicException("参数 {$field} 字符串长度不能大于 {$rules['max']}");
                }
                break;
        }
    }

    /**
     * 将 base64 图片转换为二进制
     */
    protected function base64ToFile(string $base64, array $options = []): array
    {
        // 匹配 data:image/*;base64,xxxxxxx
        if (preg_match('/^data:image\/(\w+);base64,/', $base64, $matches)) {
            $mimeType = $matches[1];
            $base64Data = substr($base64, strpos($base64, ',') + 1);
        } else {
            throw new LogicException("base64 格式错误");
        }

        // 检测是否是有效的 base64
        if (!preg_match('/^[A-Za-z0-9+\/=]+$/', $base64Data)) {
            throw new LogicException("base64 编码无效");
        }

        // 转为二进制
        $binary = base64_decode($base64Data, true);
        if ($binary === false) {
            throw new LogicException("base64 解码失败");
        }

        // 文件大小检查
        $fileSize = strlen($binary);
        $maxSize = $options['max_size'] ?? null;
        if ($maxSize && $fileSize > $maxSize) {
            $maxSizeKB = round($maxSize / 1024);
            $currentSizeKB = round($fileSize / 1024);
            throw new LogicException("文件大小超过 {$maxSizeKB}KB 限制，当前大小: {$currentSizeKB}KB");
        }

        // 文件格式检查
        $allowedTypes = $options['allowed_types'] ?? null;
        if ($allowedTypes && !in_array(strtolower($mimeType), $allowedTypes)) {
            $allowed = implode('/', $allowedTypes);
            throw new LogicException("文件格式不支持，仅支持 {$allowed}，当前格式: {$mimeType}");
        }

        return [
            'binary' => $binary,
            'filename' => $options['filename'] ?? "file.{$mimeType}",
            'mime' => "image/{$mimeType}",
        ];
    }

    /**
     * HTTP 封装
     * @throws Exception
     */
    public function sendRequest(string $method, string $url, array $query,  $body, array $headers,$customizeOptions=[]): Response
    {
        // 拼接 Query 参数
        if (!empty($query)) {
            $url .= (str_contains($url, '?') ? '&' : '?') . http_build_query($query);
        }

        $client = new Client();
        $request = $client->createRequest()
            ->setMethod($method)
            ->setUrl($url)
            ->setOptions(array_merge([
                'timeout' => 15, // 默认超时
            ],$customizeOptions));

        // GET 请求不带 Body
        if (!empty($body) && strtoupper($method) !== 'GET') {
            // 检查是否是 multipart/form-data 请求（用于文件上传）
            $contentType = $headers['Content-Type'] ?? '';
            
            if (stripos($contentType, 'multipart/form-data') !== false) {
                // 处理 multipart/form-data（文件上传）
                // 不要手动设置 Content-Type，让 Yii 自动处理 boundary
                unset($headers['Content-Type']);
                $request->addHeaders($headers);
                
                $normalData = [];
                foreach ($body as $key => $value) {
                    if (is_array($value) && isset($value['binary'], $value['filename'], $value['mime'])) {
                        // 这是一个文件字段，使用 addFileContent 直接添加二进制内容
                        $request->addFileContent($key, $value['binary'], [
                            'fileName' => $value['filename'],
                            'mimeType' => $value['mime'],
                        ]);
                    } else {
                        // 普通字段，收集到 data 中
                        $normalData[$key] = $value;
                    }
                }
                
                // 设置普通字段
                if (!empty($normalData)) {
                    $request->setData($normalData);
                }
                
            }elseif(stripos($contentType, 'application/x-www-form-urlencoded') && is_array($body)){
                $request->addHeaders($headers);
                $request->setData($body);
            }elseif (stripos($contentType, 'text/plain') !== false){
                $request->addHeaders($headers);
                $request->setContent($body);
            } else {
                // 默认使用 JSON 格式
                $request->addHeaders($headers);
                $request->setContent(json_encode($body, JSON_UNESCAPED_UNICODE));
            }
        } else {
            // GET 请求或空 body 时设置 headers
            $request->addHeaders($headers);
        }
        return $request->send();
    }


